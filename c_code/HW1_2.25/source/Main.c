#include <stdio.h>

int main() {
    // 打印字母 P
    printf("PPPPPP  \n");
    printf("P    P  \n");
    printf("P    P  \n");
    printf("PPPPPP  \n");
    printf("P       \n");
    printf("P       \n");
    printf("\n");  // 空行分隔字母

    // 打印字母 E
    printf("EEEEEE  \n");
    printf("E      \n");
    printf("E      \n");
    printf("EEEEEE  \n");
    printf("E      \n");
    printf("EEEEEE  \n");
    printf("\n");  // 空行分隔字母

    // 打印字母 N
    printf("N     N \n");
    printf("NN    N \n");
    printf("N N   N \n");
    printf("N  N  N \n");
    printf("N   N N \n");
    printf("\n");  // 空行分隔字母

    // 打印字母 G
    printf(" GGGGG  \n");
    printf("G      \n");
    printf("G      \n");
    printf("G   GGG\n");
    printf(" GGGGG  \n");

    return 0;
}